    private void cmbGarajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbGarajeActionPerformed
        int index = cmbGaraje.getSelectedIndex();
        String item = cmbGaraje.getItemAt(index);
        if (index == 0) {
        txtNombre.setText("");
        txtDireccion.setText("");
        return;
        }
        if(BLGaraje.obtenerGaraje(item)!=null){
            txtNombre.setText(BLGaraje.obtenerGaraje(item).getNombre());
            txtDireccion.setText(BLGaraje.obtenerGaraje(item).getDireccion());
        }
    }//GEN-LAST:event_cmbGarajeActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int index = cmbGaraje.getSelectedIndex();
        String item = cmbGaraje.getItemAt(index);
        if(index!=0){
            try {
                String mensaje = BLGaraje.eliminarGaraje(BLGaraje.obtenerGaraje(item).getGarajeID());
                if(mensaje==null){
                    BLRegistro.registrarActividad(empleadoID,"ADMINISTRADOR", "DELETE" , "GARAJE", "Se elimino con exito una Garaje");
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(IfrmEliminarGaraje.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed


